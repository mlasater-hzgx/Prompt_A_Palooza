export async function seedInvestigations(
  prisma: PrismaClient,
  incidents: { id: string }[],
  users: { id: string }[],
) {
  const investigations = [];
  for (const inv of INVESTIGATIONS) {
    const { incidentIndex, leadInvestigatorIndex, reviewedByIndex, startedDate, completedDate, targetCompletionDate, reviewedDate, ...data } = inv;
    const investigation = await prisma.investigation.create({
      data: {
        ...data,
        incidentId: incidents[incidentIndex].id,
        leadInvestigatorId: users[leadInvestigatorIndex].id,
        reviewedById: reviewedByIndex !== null ? users[reviewedByIndex].id : null,
        startedDate: startedDate ? new Date(startedDate + 'T00:00:00.000Z') : null,
        completedDate: completedDate ? new Date(completedDate + 'T00:00:00.000Z') : null,
        targetCompletionDate: new Date(targetCompletionDate + 'T00:00:00.000Z'),
        reviewedDate: reviewedDate ? new Date(reviewedDate + 'T00:00:00.000Z') : null,
      },
    });
    investigations.push(investigation);
  }
  return investigations;
}
