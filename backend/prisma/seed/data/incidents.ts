// Helper: create a Date at midnight UTC for a given YYYY-MM-DD
function d(dateStr: string): Date {
  return new Date(dateStr + 'T00:00:00.000Z');
}

// Helper: create a time-only value (Prisma @db.Time expects a full Date but only stores time)
function t(hours: number, minutes: number): Date {
  return new Date(`1970-01-01T${String(hours).padStart(2, '0')}:${String(minutes).padStart(2, '0')}:00.000Z`);
}

