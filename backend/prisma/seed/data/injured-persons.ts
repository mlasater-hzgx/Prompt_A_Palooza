export async function seedInjuredPersons(prisma: PrismaClient, incidents: { id: string }[]) {
  const persons = [];
  for (const ip of INJURED_PERSONS) {
    const { incidentIndex, ...data } = ip;
    const person = await prisma.injuredPerson.create({
      data: {
        ...data,
        incidentId: incidents[incidentIndex].id,
        returnDate: data.returnDate ? new Date(data.returnDate + 'T00:00:00.000Z') : null,
      },
    });
    persons.push(person);
  }
  return persons;
}
