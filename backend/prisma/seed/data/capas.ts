export async function seedCapas(
  prisma: PrismaClient,
  incidents: { id: string }[],
  investigations: { id: string }[],
  users: { id: string }[],
) {
  // Create CAPA number sequences
  await prisma.capaNumberSequence.createMany({
    data: [
      { year: 2024, lastSeq: 11 },
      { year: 2025, lastSeq: 38 },
      { year: 2026, lastSeq: 2 },
    ],
  });

  const capas = [];
  for (const capa of CAPAS) {
    const {
      incidentIndex,
      investigationIndex,
      assignedToIndex,
      verifiedByIndex,
      assignedDate,
      dueDate,
      completedDate,
      verificationDueDate,
      verifiedDate,
      cost,
      ...data
    } = capa;

    const created = await prisma.capa.create({
      data: {
        ...data,
        incidentId: incidents[incidentIndex].id,
        investigationId: investigationIndex !== null ? investigations[investigationIndex].id : null,
        assignedToId: users[assignedToIndex].id,
        verifiedById: verifiedByIndex !== null ? users[verifiedByIndex].id : null,
        assignedDate: new Date(assignedDate + 'T00:00:00.000Z'),
        dueDate: new Date(dueDate + 'T00:00:00.000Z'),
        completedDate: completedDate ? new Date(completedDate + 'T00:00:00.000Z') : null,
        verificationDueDate: verificationDueDate ? new Date(verificationDueDate + 'T00:00:00.000Z') : null,
        verifiedDate: verifiedDate ? new Date(verifiedDate + 'T00:00:00.000Z') : null,
        cost: cost,
      },
    });
    capas.push(created);
  }
  return capas;
}
